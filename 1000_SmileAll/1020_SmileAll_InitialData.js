function createInitialCell(stage, vSize, hSize) {
    const initialArray = initialCell[stage];
    const cell = [];

    const vLimit = initialArray.length;
    for (let v = 0; v < vSize; v++) {
        let element;
        if (v < vLimit) element = initialArray[v]; else element = "";
        const hLimit = element.length;
        for (let h = 0; h < hSize; h++) {
            const c = v * hSize + h;
            if (h < hLimit) {
                if (element.charAt(h) === "0") {
                    cell[c] = statusNum.smile;
                } else {
                    cell[c] = statusNum.angry;
                }
            } else {
                cell[c] = statusNum.wall;
            }
        }
    }
    return cell;
}

